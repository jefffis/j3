			<h2>Eh, Big Deal&mdash;the Logo&rsquo;s the Same</h2>
			<p>To the laymen, perhaps it&rsquo;s no big deal. But to design nitpickers like me, it <strong>IS</strong> a big deal. More importantly, subconsciously it&rsquo;s a big deal to the laymen. All the subtle design choices from the website are nowhere to be found in the email. Instead; new, poorly chosen elements have been added:</p>
			</article>
			<article class="full">
			<ul>
				<li>the logo is right-aligned <em>(a big email no-no)</em> AND has an odd green border added on the right</li>
				<li>a thick, heavy-feeling black border outlines the entire active area</li>
				<li>font colors and links very greatly from the website</li>
				<li>the headline of the email feels like a great afterthought</li>
				<li>the heinous use of amateurish drop-shadows on the buttons is disruptive (while disruptive can be good, this isn&rsquo;t the case here)</li>
				<li>the subtle color sections from the site disappear, making the whole flow of content non-exisitent</li>
			</ul>
			<p>All the design choices made on the website [seemingly] were done with care and consideration of the users and the business objectives. The email looks like it was slapped into a free email template <em>(like all Constant Contact templates....yuck)</em> and sent off. Perhaps the person in charge knew that email has great ROI. <strong>However, as with most of the world, not all email is created equal</strong>.</p>
			<p>I&rsquo;d be curious see what the metrics of this email were. Even if they were decent, I guarantee a bit of planning and professional design care would help these campaigns reach the next level.</p>
