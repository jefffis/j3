	<h2>Check your settings</h2>
	<p>Setting up <a href="http://windows.microsoft.com/en-US/windows7/products/features/windows-update">Windows Update is simple</a>: Just <a href="http://www.update.microsoft.com/windowsupdate/v6/thanks.aspx?ln=en&&thankspage=5">go to the Microsoft Update website</a>. If you&rsquo;ve already got automatic updating turned on, Windows Update in Control Panel will open and show your update status. If it&rsquo;s not yet turned on, you'll be guided through the steps to do so. After that, all the latest security and performance improvements will be installed on your PC quickly and reliably.</p>
	<h2>It&rsquo;s (mostly) automatic</h2>
	<p>When you turn on automatic updating, most updates will download and install without you having to lift a finger. But sometimes Windows Update will need your input during an installation. In this case, you&rsquo;ll see an alert in the notification area at the far right of the taskbar&mdash;be sure to click it. If you don't respond to a Windows Update alert, your PC might end up missing an important download.</p>

